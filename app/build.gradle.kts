    signingConfigs {
        create("release") {
            if (keystorePropertiesFile.exists()) {
                storeFile = file(keystoreProperties.getProperty("storeFile"))
                storePassword = keystoreProperties.getProperty("storePassword")
                keyAlias = keystoreProperties.getProperty("keyAlias")
                keyPassword = keystoreProperties.getProperty("keyPassword")
            }
        }
    }

    buildTypes {
        // Release build - optimized for distribution
        release {
            isMinifyEnabled = true
            isShrinkResources = true
            proguardFiles(
                getDefaultProguardFile("proguard-android-optimize.txt"),
                "proguard-rules.pro"
            )
            if (keystorePropertiesFile.exists()) {
                signingConfig = signingConfigs.getByName("release")
            }
        }

        // Fast release-like build for local testing.
        // Keeps runtime closer to release (non-debuggable) but skips expensive shrink/obfuscation.
        create("fast") {
            initWith(getByName("release"))
            isMinifyEnabled = false
            isShrinkResources = false

            // Keep behavior close to release by avoiding debug runtime flags.
            isDebuggable = false

            // Use debug signing for easy local install when release keystore is unavailable.
            signingConfig = signingConfigs.getByName("debug")

            // Reuse release-compatible dependency/resource variants when needed.
            matchingFallbacks += listOf("release")
        }
        
    }
    
    compileOptions {
        sourceCompatibility = JavaVersion.VERSION_11
        targetCompatibility = JavaVersion.VERSION_11
    }
    
    buildFeatures {
        compose = true
    }
